#include "libft.h"

int	ft_strchr_i(const char *s, char c)
{
	int	i;

	i = 0;
	while (s && s[i])
	{
		if (s[i] == c)
			return (i);
		i++;
	}
	if (!c && !s)
	{
		return (0);
	}
	if (s && s[i] == c)
		return (i);
	return (-1);
}
